# Pin Bazel 8 to avoid Bazel 9 compatibility issues with native rules
export USE_BAZEL_VERSION=8.2.1

# Trigger setup_configs rule of fuzztest to generate oss-fuzz bazel config
bazel run @com_google_fuzztest//bazel:setup_configs >> /etc/bazel.bazelrc

# tcmalloc uses Google FuzzTest with cc_test rules
FUZZ_TARGETS=$(bazel query 'kind("cc_test", filter("_fuzz", //tcmalloc/...))' 2>/dev/null)

echo "Found fuzz targets:"
echo "$FUZZ_TARGETS"

if [ -z "$FUZZ_TARGETS" ]; then
    echo "ERROR: No fuzz targets found"
    exit 1
fi

# Build fuzz targets with oss-fuzz config (generated by setup_configs)
# Link fuzzer_no_main to provide LLVMFuzzerRunDriver needed by FuzzTest compatibility mode
bazel build --config=oss-fuzz --subcommands \
    --linkopt=/usr/local/lib/clang/22/lib/x86_64-unknown-linux-gnu/libclang_rt.fuzzer_no_main.a \
    $FUZZ_TARGETS

# Get output paths
FUZZ_TEST_BINARIES_OUT_PATHS=$(bazel cquery --config=oss-fuzz "kind(\"cc_test\", filter(\"_fuzz\", //tcmalloc/...))" --output=files 2>/dev/null)

# Create wrapper scripts following FuzzTest OSS-Fuzz convention
for fuzz_main_file in $FUZZ_TEST_BINARIES_OUT_PATHS; do
  FUZZ_TESTS=$($fuzz_main_file --list_fuzz_tests 2>/dev/null || true)
  cp ${fuzz_main_file} $OUT/
  fuzz_basename=$(basename $fuzz_main_file)
  chmod -x $OUT/$fuzz_basename
  if [ -n "$FUZZ_TESTS" ]; then
    for fuzz_entrypoint in $FUZZ_TESTS; do
      # Skip lines that are not test names (e.g. "[*] Fuzz test:" prefix)
      echo "$fuzz_entrypoint" | grep -q '\.' || continue
      TARGET_FUZZER="${fuzz_basename}@${fuzz_entrypoint}"
      echo "#!/bin/sh
# LLVMFuzzerTestOneInput for fuzzer detection.
this_dir=\$(dirname \"\$0\")
chmod +x \$this_dir/$fuzz_basename
\$this_dir/$fuzz_basename --fuzz=$fuzz_entrypoint -- \$@" > $OUT/$TARGET_FUZZER
      chmod +x $OUT/$TARGET_FUZZER
    done
  fi
done
